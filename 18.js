//  Basic Math Operations in JavaScript

let a = 15;
let b = 4;
console.log("Values: a =", a, ", b =", b);
console.log("Addition: ", a + b); // Output: 19
console.log("Subtraction: ", a - b); // Output: 11
console.log("Multiplication: ", a * b); // Output: 60
console.log("Division: ", a / b); // Output: 3.75
console.log("Modulus: ", a % b); // Output: 3, the remainder of 15 divided by 4
console.log("Exponentiation: ", a ** b); // Output: 50625
console.log("Floor Division: ", Math.floor(a / b)); // Output: 3, division rounded down to nearest integer
console.log("Increment a: ", ++a); // Output: 16
console.log("Decrement b: ", --b); // Output: 3
console.log("Negation of a: ", -a); // Output: -16
console.log("Absolute value of -b: ", Math.abs(-b)); // Output: 3, the absolute value of -3
// pi value
console.log("Value of pi: ", Math.PI); // Output: 3.141592653589793
